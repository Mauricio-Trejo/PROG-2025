# Pregunta 8: Explica los conceptos usando un ejemplo de una capacidad humana
#   Respuesta:
#   1. Unidad Central de Procesamiento: el CPU sería como el cerebro humano,
#   procesa toda la información y manda a ejecutar todas las instrucciones al
#   resto del cuerpo.
#   2. Memoria principal: sería como la memoria a corto plazo de una persona,
#   se guarda información de manera temporal.
#   3. Memoria secundaria: sería como la memoria a largo plazo, la información
#   estará ahí de manera permanente, también podría ser como una carpeta externa
#   a la que podríamos acceder cuando nosotros querramos.
#   4. Dispositivos de entrada: sería como si una persona te estuviera hablando
#   para contarte algo, te esta dando información para que tu la proceses.
#   5. Dispositivos de salidad: con la misma analogía de los dispositivos de entrada,
#   los dispositivos de salida sería tú respondiendo después de haber escuchado la
#   historia recién contada.

